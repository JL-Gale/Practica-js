//Opdores de comparacion 

let a = 20;
let b = "20";

//Igualdad ==
console.log(a == b);

//Igualdad Eestricta ===
console.log(a === b);


//Desigualdad !=
console.log(a != b);

//Desigualdad estricta !==
console.log(a !== b);

//Mayor que >
a = 21;
console.log(a > b);

//Mayor o igual que >=
a = 20;
console.log(a >= b);

//Menor que <
a = 20;
console.log(a < b);

//Menor o igual <=
a = 19;
console.log(a <= b);