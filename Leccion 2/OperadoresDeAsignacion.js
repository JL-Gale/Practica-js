

//Operadores de asignacion

let numero = 10;
console.log(numero);

numero = 20;
console.log(numero);

//Operador +=
numero += 50;
console.log(numero);

//Operador -=
numero -=20;
console.log(numero);

//Operador *=
numero *= 2;
console.log(numero);

//Operador /=
numero /= 2;
console.log(numero);

//Operador %=
numero %= 3;
console.log(numero);

//Operador **/
numero **=4;
console.log(numero);